# List - Indexing
# Indexing is what you call when you approach the data of a certain list with a number that shows the cordinates of that data
# Ex)
a = [1,2,3,4]
print(f"a[0] : {a[0]}")
print(f"a[-1] : {a[-1]}")

a = [1,2,3,['a','b','c']]
print(f"a[0] : {a[0]}")
print(f"a[-1] : {a[-1]}")
print(f"a[-1][0] : {a[-1][0]}")
